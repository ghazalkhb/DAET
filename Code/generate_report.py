def tex(s):
    if s is None:
        return "---"
    s = str(s)
    for old, new in [
        ("\\", "\\textbackslash{}"), ("&", "\\&"), ("%", "\\%"),
        ("$", "\\$"), ("#", "\\#"), ("_", "\\_"), ("{", "\\{"),
        ("}", "\\}"), ("~", "\\textasciitilde{}"), ("^", "\\^{}"),
    ]:
        s = s.replace(old, new)
    return s

def fmt(v, dec=2):
    if v is None or (isinstance(v, float) and v != v):
        return "---"
    return f"{v:.{dec}f}"

